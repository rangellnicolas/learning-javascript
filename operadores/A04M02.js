// Atribuição: =, +=, -=, *=, etc. Atribui valores a variáveis. Exemplo: a = 10; a += 5; é o mesmo que a = a + 5.

let a = 10;    // atribuição simples
a += 5;        // a = a + 5;   resultado: 15
a -= 3;        // a = a - 3;   resultado: 12
a *= 2;        // a = a * 2;   resultado: 24
a /= 4;        // a = a / 4;   resultado: 6
a %= 5;        // a = a % 5;   resultado: 1
a **= 3;       // a = a ** 3;  resultado: 1
