// Arrays são listas ordenadas de valores. Declare com colchetes []. Por exemplo:
let numeros = [1, 2, 3, 4, 5]; // Array de números
let frutas = ['maçã', 'banana', 'laranja']; // Array de strings

// Cada elemento tem índice iniciando em 0. 
// A propriedade length informa quantos elementos há. Exemplos:

let tamanhoNumeros = numeros.length; // 5
let primeiraFruta = frutas[0]; // 'maçã' (primeiro elemento)
let ultimaFruta = frutas[frutas.length - 1]; // 'laranja'

// (Em JavaScript, arrays começam no índice zero.) 
// Você pode criar arrays de diferentes formas, inclusive mistos:

let misto = [1, 'texto', true, null]; // Array com diferentes tipos